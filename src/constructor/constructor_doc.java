package constructor;

public class constructor_doc {

	/*
	 * -------constructor------------
	 *  - used to create object in java
	 *  - special type of method which has same name as class name 
	 *    is know as constructor
	 *  - if there no any user defined constructor in class the java 
	 *    provides default constructor
	 *    
	 *  - no return type
	 *  - types:
	 *  
	 *    1.default constructor :
	 *    - use to create object and set default values to it
	 *    syntax:
	 *     
	 *     class_name(){
	 *     //set default values to object
	 *     }
	 *  
	 *  
	 *    2. parameterized constructor:
	 *    - use to create object and set dynamic values to it
	 *     syntax:
	 *     
	 *     class_name(arg1,arg2,.....){
	 *      // set dynamic values to it 
	 *     }
	 */
}
