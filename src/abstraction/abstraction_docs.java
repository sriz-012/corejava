package abstraction;

public class abstraction_docs {

	/*
	 * ---------abstraction---------
	 * 
	 * process of implementation details in the program is know as abstraction
	 * 
	 * to achieve abstraction
	 * a> abstract class:
	 *   #class class which contain abstract(unimplemented) method and non - abstract method
	 *   a> abstract method
	 *   e.g abstract void print();
	 *   abstract int getArea(int l,int b);
	 *   
	 *   #every abstract class must have atleast one child class and child chlass must override abstract methods
	 *   
	 *   #we cant create of abstract class
	 *   #level of abstraction 0-100%
	 *   
	 *   #syntax ;
	 *   abstract class class_name{
	 *   // abstract method
	 *   
	 *   //non-abstract methods
	 *   
	 *   b>interface:
	 *   
	 *   # same as class which contains public abstract methods and public static final variables only
	 *   # every interface must have atleast one implementation class and impl class must override abstract methods.
	 *   # we cant create object of interface.
	 *   #level of abstraction 100%
	 *   
	 *   syntax:
	 *   interface interface_name{
	 *   
	 *   //public abstract methods
	 *   
	 *   }
	 */
}
